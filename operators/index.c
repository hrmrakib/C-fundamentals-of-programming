#include <stdio.h>

int main() {
   int a = 21;
   int b = 8;
   
   int sum = a + b;
   int sub = a - b;
   int multiple = a * b;
   int division = a / b;
  
   printf("Sum: %d\n", sum);
   printf("Sub: %d\n", sub);
   printf("Multiple: %d\n", multiple);
   printf("Division: %d\n", division);

   return 0;
}